def get_hero_by_number(number: int):
    """Получить героя по номеру"""
    return HEROES_BY_NUMBER.get(number)


def get_hero_by_name(name: str):
    """Получить героя по имени"""
    for hero in HEROES:
        if hero["name"] == name:
            return hero
    return None


def get_heroes_by_author(author: str):
    """Получить всех героев автора"""
    return [h for h in HEROES if h["author"] == author]


def get_heroes_by_work(work: str):
    """Получить всех героев произведения"""
    return [h for h in HEROES if h["book"] == work]


def get_authors():
    """Получить список всех авторов"""
    authors = set()
    for hero in HEROES:
        authors.add(hero["author"])
    return sorted(authors)


def get_works():
    """Получить список всех произведений"""
    works = set()
    for hero in HEROES:
        works.add(hero["book"])
    return sorted(works)


def get_total_heroes():
    """Получить общее количество героев"""
    return len(HEROES_BY_NUMBER)


def get_heroes_grouped():
    """Получить героев, сгруппированных по автору и произведению"""
    result = {}
    for hero in HEROES:
        author = hero["author"]
        work = hero["book"]
        if author not in result:
            result[author] = {}
        if work not in result[author]:
            result[author][work] = []
        result[author][work].append(hero)
    return result


def get_rarity_count():
    """Получить количество героев по редкости"""
    result = {"легендарный": 0, "эпический": 0, "редкий": 0, "обычный": 0}
    for hero in HEROES:
        rarity = hero.get("rarity", "обычный")
        if rarity in result:
            result[rarity] += 1
    return result