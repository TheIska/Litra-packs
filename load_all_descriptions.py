    print("=" * 60)
    print(f"📊 Всего описаний: {len(DESCRIPTIONS)}")
    print("-" * 60)
    
    # Проверяем, есть ли БД
    os.makedirs(os.path.dirname(DB_PATH), exist_ok=True)
    
    conn = sqlite3.connect(DB_PATH)
    c = conn.cursor()
    
    # Создаём таблицу
    c.execute('''
        CREATE TABLE IF NOT EXISTS hero_descriptions (
            hero_key TEXT PRIMARY KEY,
            description TEXT,
            created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
        )
    ''')
    
    success = 0
    fail = 0
    
    for hero_key, description in DESCRIPTIONS.items():
        try:
            c.execute(
                "INSERT OR REPLACE INTO hero_descriptions (hero_key, description) VALUES (?, ?)",
                (hero_key, description)
            )
            success += 1
            print(f"✅ {hero_key[:40]}...")
        except Exception as e:
            fail += 1
            print(f"❌ {hero_key[:40]}: {e}")
    
    conn.commit()
    conn.close()
    
    print("-" * 60)
    print("📊 РЕЗУЛЬТАТЫ:")
    print(f"   ✅ Загружено: {success}")
    print(f"   ❌ Ошибок: {fail}")
    print("=" * 60)


